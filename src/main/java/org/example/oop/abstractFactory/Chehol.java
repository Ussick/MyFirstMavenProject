package org.example.oop.abstractFactory;

public class Chehol {
    public String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Chehol{" +
                "name='" + name + '\'' +
                '}';
    }
}
